                <h3>YOULP’ers organiseren “Hart voor Oekraïne-middag’</h3>
                <h6>Maastricht, 04 - 10 - 2022</h6>
                <p class="introduction">
                    Op KC Leyenbroek hebben de junior medewerkers 
                    van YOULP in samenwerking met kinderopvang 
                    de Rode Loper een ‘Hart voor Oekraïne middag’. 
                    In de Zuilenzaal van KC Leyenbroek was een 
                    grootschalige markt opgezet. Vaders, moeders, 
                    opa’s, oma’s en kinderen waren van harte 
                    welkom om een zelfgemaakt (knutsel)werkje, 
                    hapje of drankje te kopen voor het goede doel.
                    De opkomst was waanzinnig en vele (knutsel)werkjes 
                    en lekkernijen vonden hun weg naar huis. 
                    Kinderen die meehielpen met de verkoop genoten 
                    van hun rol en van de gulle giften. Ook waren 
                    er leerlingen die een eigen inzamelactie hielden 
                    en zo veel geld inzamelden voor deze middag. Het 
                    was een groot succes. In totaal is er maar 
                    liefst € 2.851,24 opgehaald! 
                </p>
